package com.ejemplo;

public class Sumador {

    /**
     * Función que toma dos números enteros como entrada y devuelve su suma.
     *
     * @param num1 El primer número entero.
     * @param num2 El segundo número entero.
     * @return La suma de num1 y num2.
     */
    public static int sumar(int num1, int num2) {
        return num1 + num2;
    }
}